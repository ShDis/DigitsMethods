#Решите задачу Коши методом Эйлера

# Определение функции f(x, y)
def f(x,y):
    return -(1+x*y)/(x*x)

def euler_method(f, a, b, y0, h):
    """
    Метод Эйлера для решения задачи Коши
    :param f: Функция правой части уравнения y' = f(x, y)
    :param a: Левый конец интервала
    :param b: Правый конец интервала
    :param y0: Начальное условие y(a) = y0
    :param h: Шаг
    :return: Список значений y(x) на равномерной сетке с шагом h
    """
    n = int((b - a) / h) + 1  # Количество точек на сетке
    x = [a + i * h for i in range(n)]  # Сетка по x
    y = [y0]  # Начальное значение y(a)
    for i in range(n - 1):
        # Метод Эйлера: y_{i+1} = y_i + h * f(x_i, y_i)
        y.append(y[i] + h * f(x[i], y[i]))
    return y

# Исходные данные
a = 1
b = 2
y0 = 1
h = 0.1  # Шаг

# Решение задачи Коши методом Эйлера
y = euler_method(f, a, b, y0, h)

# Вывод результатов
print("x\t\ty(x)")
for i in range(len(y)):
    x = a + i * h
    print("{:.1f}\t\t{:.6f}".format(x, y[i]))